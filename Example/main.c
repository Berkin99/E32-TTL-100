static uint8_t buffer [30]; 						// RX Buffer


#ifdef TX_MODULE
static uint8_t txdata[] = "HelloWorld\r\n"; 		// TX Data Example
#endif

/* For Receive Unknown Length Data : E32100_Receive_DMA
 * This way processor is free while data is receiving.
 * When Receive is complete RxEventCallback function called.
 * For continious receiving, E32100_Receive_DMA function
 * called again.
 *
 * While module is in the receive dma mode ,it can't transmit
 * without abort receive. This is not the best solution for
 * two way communication.
 *  */
void HAL_UARTEx_RxEventCallback(UART_HandleTypeDef* huart, uint16_t Size) {
	if(huart->Instance == USART1){
		E32100_Receive_DMA(buffer);		// Listen again.
	}
}


int main(void)
{
  HAL_Init();
  SystemClock_Config();

  MX_GPIO_Init();
  MX_DMA_Init();
  MX_USART2_UART_Init();
  MX_USART1_UART_Init();

  /* E32100 Initialization
   * M0  : PC0 (output)
   * M1  : PC1 (output)
   * AUX : PC2 (input)
   * */
  E32100_Init(&huart1, GPIOC, GPIO_PIN_0, GPIOC, GPIO_PIN_1, GPIOC, GPIO_PIN_2);

  /* Config Change Example :
   * WARNING Config change must be executed same on both rx and tx modules.
   *
   * E32100_Config_t config;
   * E32100_GetDefaultConfig(&config);
   * config.sped.airDataRate = E32100_ADR_4800; 	// Faster air data rate.
   * E32100_SetConfig(config, false);				// False = Do not save the configuration.
   *
   */

#ifdef RX_MODULE
  E32100_Receive_DMA(buffer);
#endif

  while (1)
  {
	  /* Code */

#ifdef TX_MODULE
	  E32100_Transmit(txdata,(sizeof(txdata)/sizeof(txdata[0])));
#endif

	  HAL_Delay(1000);
  }

}

